    private void showOldData() {
        try {
            Employee emp = hService.getEmployeeById(selectedRow);
            nameTxt.setText(emp.getEmp_name());
            surnameTxt.setText(emp.getEmp_surname());
            genderCmb.setSelectedItem(emp.getEmp_gender());
            dobPicker.setDate(emp.getEmp_dob());
            phoneTxt.setText(emp.getEmp_phone());
            addressTxt.setText(emp.getEmp_address());
            salaryTxt.setText(Double.toString(emp.getEmp_salary()));
            emailTxt.setText(emp.getEmp_email());
            bonusTxt.setText(Double.toString(emp.getEmp_bonus()));
            ctgTxt.setText(emp.getEmp_category());
            showDeptCombo(emp.getEmp_department().getId(), emp.getEmp_department().getDept_name());
            hirePicker.setDate(emp.getEmp_hire_date());
            firePicker.setDate(emp.getEmp_fire_date());
            activeTxt.setText(Integer.toString(emp.getIsActive()));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void showDeptCombo(int id, String value) {
        try {
            DefaultComboBoxModel deptCmb = (DefaultComboBoxModel) this.deptCmb.getModel();
            List<Department> deptList = hService.getDeptList();
            for(Department dpt: deptList) {
                deptCmb.addElement(new Item(dpt.getId(), dpt.getDept_name()));
            }
            deptCmb.setSelectedItem(new Item(id, value));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
