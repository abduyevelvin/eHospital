    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        try {
            Item deptCombo = (Item) deptCmb.getSelectedItem();
            Employee emp = new Employee();
            Department dept = new Department();
            dept.setId(deptCombo.getId());
            emp.setEmp_department(dept);
            emp.setEmp_name(nameTxt.getText());
            emp.setEmp_surname(surnameTxt.getText());
            emp.setEmp_gender((String) genderCmb.getSelectedItem());
            emp.setEmp_dob(dobPicker.getDate());
            emp.setEmp_phone(phoneTxt.getText());
            emp.setEmp_address(addressTxt.getText());
            emp.setEmp_salary(Double.parseDouble(salaryTxt.getText()));
            emp.setEmp_email(emailTxt.getText());
            emp.setEmp_bonus(Double.parseDouble(bonusTxt.getText()));
            emp.setEmp_category(ctgTxt.getText());
            emp.setEmp_hire_date(hirePicker.getDate());
            emp.setEmp_fire_date(firePicker.getDate());
            emp.setIsActive(Integer.parseInt(activeTxt.getText()));
            boolean isUpdated = hService.updateEmployee(emp, selectedRow);
            if(isUpdated) {
                JOptionPane.showMessageDialog(null, "Employee has been successfully updated!", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Patient cannot be updated!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_editBtnActionPerformed

