    private void emailTxtFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_emailTxtFocusLost
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\."+
                            "[a-zA-Z0-9_+&*-]+)*@" +
                            "(?:[a-zA-Z0-9-]+\\.)+[a-z" +
                            "A-Z]{2,7}$";
        Pattern pat = Pattern.compile(emailRegex);
        if(!(pat.matcher(emailTxt.getText()).matches()) || (emailTxt.getText() == null)) {
            JOptionPane.showMessageDialog(null, "Please use correct email format!", "Warning", JOptionPane.WARNING_MESSAGE);
            emailTxt.requestFocus();
        }
    }//GEN-LAST:event_emailTxtFocusLost

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            Item deptCombo = (Item)deptCmb.getSelectedItem();
            Employee emp = new Employee();
            Department department = new Department();
            department.setId(deptCombo.getId());
            emp.setEmp_name(nameTxt.getText());
            emp.setEmp_surname(surnameTxt.getText());
            emp.setEmp_gender((String) genderCmb.getSelectedItem());
            emp.setEmp_dob(dobPicker.getDate());
            emp.setEmp_phone(phoneTxt.getText());
            emp.setEmp_email(emailTxt.getText());
            emp.setEmp_address(addressTxt.getText());
            emp.setEmp_salary(Double.parseDouble(salaryTxt.getText()));
            emp.setEmp_bonus(Double.parseDouble(bonusTxt.getText()));
            emp.setEmp_category(ctgTxt.getText());
            emp.setEmp_department(department);
            emp.setEmp_hire_date(hirePicker.getDate());
            boolean isAdded = hService.addEmployee(emp);
            if(isAdded) {
                JOptionPane.showMessageDialog(null, "Employee has been added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                clearBtnActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(null, "Employee cannot be added!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_saveBtnActionPerformed

