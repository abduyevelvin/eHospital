    private void showDeptCombo() {
        try {
            DefaultComboBoxModel deptCmb = (DefaultComboBoxModel) this.deptCmb.getModel();
            List<Department> deptList = hService.getDeptList();
            for(Department dpt: deptList) {
                deptCmb.addElement(new Item(dpt.getId(), dpt.getDept_name()));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
